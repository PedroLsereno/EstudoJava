package entities;

public class Triangulo {
	//Criando os atributos da classe
	public double a;
	public double b;
	public double c;
	
	//Como esse metodo não vai recebe nada além das variáveis a,b,c, então o () pode ficar vazio.
	public double area() {
		double p = (a + b + c) / 2.0;
		double resultado = Math.sqrt(p * (p - a) * (p - b) * (p - c));
		return resultado;
	}
}
